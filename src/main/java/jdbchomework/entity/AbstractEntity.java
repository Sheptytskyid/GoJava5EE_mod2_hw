package jdbchomework.entity;

public class AbstractEntity {

    private long id;
    private String name;

    public AbstractEntity(long id, String name) {
        this.id = id;
        this.name = name;
    }

    public long getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
